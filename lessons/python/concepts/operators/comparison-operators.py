def calc(a, b):
    print(f"{a}\t> \t{b}\t->\t{a>b}")
    print(f"{a}\t>=\t{b}\t->\t{a>=b}")
    print(f"{a}\t< \t{b}\t->\t{a<b}")
    print(f"{a}\t<=\t{b}\t->\t{a<=b}")
    print(f"{a}\t==\t{b}\t->\t{a==b}")
    print(f"{a}\t!=\t{b}\t->\t{a!=b}")
    print()


###~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
calc(10, 10)
# 10	>	10	->	False
# 10	>=	10	->	True
# 10	<	10	->	False
# 10	<=	10	->	True
# 10	==	10	->	True
# 10	!=	10	->	False

###~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
