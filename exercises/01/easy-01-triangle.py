for i in range(6):
    print("*" * i)


print()

###~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
N = 6
for i in range(N):
    print(" " * (N - i) + "*" * i)


print()

###~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
for i in [1, 3, 5, 7]:
    print(("*" * i).center(7))


print()

###~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
for i in [7, 5, 3, 1]:
    print(("*" * i).center(7))

print()

###~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
for i in range(random.randint(5,10)):
    print(("*" * random.choice([1,3,5,7])).center(7))

print()

