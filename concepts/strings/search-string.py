s = "Hello World"


# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
print("Find:", s.find("o"))
# Find: 4


print('Right Find:', s.rfind("o"))
# Right Find: 7


# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
print("Index:", s.index("o"))
# Index: 4


print("Right Index:", s.rindex("o"))
# Right Index: 7